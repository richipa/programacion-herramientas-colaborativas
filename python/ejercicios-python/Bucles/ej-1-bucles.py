'''
Descripción:
Escribe un programa que imprima los números del 1 al 5 usando un bucle for con range.

Instrucciones:
Utiliza un bucle for con range(1, 6) para iterar del 1 al 5.
En cada iteración, imprime el número actual.

'''
numero=0  #Definimos la variable "numero" a 0.


#El bucle "for" nos dice que para el "numero" dentro de ese "rango" (que definimos nosotros del 1 al 6), imprimeme por pantalla de uno en uno los numeros.

for numero in range (1, 6):
    print(f"Número {numero}")

